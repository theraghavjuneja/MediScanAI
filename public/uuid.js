/**
 * UUID v4 generator for client-side usage
 * Since we can't import the uuid package directly in the artifact,
 * this provides a basic implementation for the demo
 */
function uuidv4() {
  return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, function(c) {
    var r = Math.random() * 16 | 0, v = c == 'x' ? r : (r & 0x3 | 0x8);
    return v.toString(16);
  });
}